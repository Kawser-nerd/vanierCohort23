using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace demoWk4.Model
{
    class modelWithoutContructor
    {
        public double addition(double var1, double var2)
        {
            return var1 + var2;
        }
        public double substraction(double var1, double var2)
        {
            return var1 - var2;
        }
        public double multiplication(double var1, double var2)
        {
            return var1 * var2;
        }
        public double division(double var1, double var2)
        {
            return var1 / var2;
        }
    }
}
